package ec.ups.edu.p3.entidades;

/**
 * 
 * @author fmont
 *
 */
public class Lote {

	private int x;
	private int y;
	private int lote;
	private boolean estado = false;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public boolean getEstado() {
		return estado;
	}

	public void setEstado(boolean estado) {
		this.estado = estado;
	}

	public int getLote() {
		return lote;
	}

	public void setLote(int lote) {
		this.lote = lote;
	}

}
